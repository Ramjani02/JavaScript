//Switch case.js:
/* Switch case:
 Lift concepts:
 Switch(expression)
 {
 case condition1: statement(s)
 break;
 case condition2: statement(s)
 break;
 case condition n: statement(s)
 break;
 default:statement(s)
 }
*/ 
let choice=4;
switch(choice){
 case 1:
 document.write("<h1> This is first floor </h1>");
 break;
 case 2:
 document.write("<h1> This is second floor </h1>");
 break;
 case 3:
 document.write("<h1> This is third floor </h1>");
 break;
 case 4:
 document.write("<h1> This is fourth floor </h1>");
 break;
 case 5:
 document.write("<h1> This is fifth floor </h1>");
 break;
 default:
 document.write("<h1> Invalid Choice </h1>");
 break;
}